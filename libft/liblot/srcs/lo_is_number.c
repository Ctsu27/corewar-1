#include "../includes/libft.h"

int			lo_is_number(char *str)
{
	int i;

	i = 0;
	if (str[0] == '-')
		++i;
	while (str[i])
	{
		if (lo_isdigit(str[i]) == 0)
			return (0);
		++i;
	}
	return (1);
}
