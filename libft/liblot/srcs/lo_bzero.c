#include "../includes/libft.h"

void	lo_bzero(void *s, size_t n)
{
	char	*ptr;
	int		i;

	i = 0;
	if (!(s))
		return ;
	ptr = s;
	while (n > 0)
	{
		ptr[i] = '\0';
		++i;
		n--;
	}
}
