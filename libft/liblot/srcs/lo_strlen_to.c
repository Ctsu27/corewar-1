#include "../includes/libft.h"

int		lo_strlen_to(const char *s, char c)
{
	int i;

	if (!s)
		return (-1);
	i = 0;
	while (s[i])
	{
		if (s[i] == c)
			return (i);
		i++;
	}
	return (0);
}
